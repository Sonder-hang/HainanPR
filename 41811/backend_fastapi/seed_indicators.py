def seed():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # 验证表存在
    cur.execute("SELECT name FROM sqlite_master WHERE type='table' AND name='indicator'")
    if not cur.fetchone():
        print("❌  表 'indicator' 不存在，请先运行 FastAPI 初始化。")
        conn.close()
        sys.exit(1)

    # 清理旧数据
    cur.execute("DELETE FROM indicator")
    conn.commit()

    # ---- seed 四要素指标 ----
    four_count = 0
    for (name, seq, category, scope, work_content, rule_logic) in FOUR_ELEMENTS:
        cur.execute("""
            INSERT INTO indicator
              (name, indicator_type, category, seq, scope, work_content, rule_logic,
               calc_method, sql_content, prompt_content, status, is_computable,
               numerator_desc, denominator_desc, numerator_sql, denominator_sql,
               involved_tables, use_llm, platform_data_ready, priority, remark,
               regex_match, regex_rule, description, formula)
            VALUES (?, ?, ?, ?, ?, ?, ?, 'none', '', '', 'pending', 1,
               '', '', '', '', '[]', 0, 0, '', '', 0, '', '', '')
        """, (name, "four", category, seq, scope, work_content, rule_logic))
        four_count += 1

    # ---- seed 十八项指标 ----
    core_count = 0
    for row in CORE18:
        name, denom, numer, desc, dp, ds, np, ns, remark, regex_rule = row
        # description 字段同时存放说明和口径，方便后续读取
        desc_parts = []
        if desc and desc != "无":
            desc_parts.append(desc)
        if denom:
            desc_parts.append(f"【分母口径】{denom}")
        if dp or ds:
            desc_parts.append(f"分母采集方案：{dp}（来源：{ds}）")
        if numer:
            desc_parts.append(f"【分子口径】{numer}")
        if np or ns:
            desc_parts.append(f"分子采集方案：{np}（来源：{ns}）")

        full_desc = "\n".join(desc_parts)
        regex_match = 1 if regex_rule else 0

        vals = [
            name, "core18", core_count + 1, full_desc, numer, denom,
            remark, regex_rule, regex_match,
            "none", "", "",
            "pending", 1, 0,
            "[]", 0, "",
            "", "", "", "", "",
            "", "",
        ]
        cur.execute(f"""
            INSERT INTO indicator
              (name, indicator_type, seq, description,
               numerator_desc, denominator_desc, remark, regex_rule, regex_match,
               calc_method, sql_content, prompt_content,
               status, is_computable, use_llm,
               involved_tables, platform_data_ready, priority,
               formula, category, scope, work_content, rule_logic,
               numerator_sql, denominator_sql)
            VALUES ({", ".join(["?"] * len(vals))})
        """, vals)
        core_count += 1

    conn.commit()
    print(f"✅  写入完成：四要素 {four_count} 条，十八项 {core_count} 条")
    print(f"    indicator 表共 {four_count + core_count} 条记录")

    # 验证
    cur.execute("SELECT indicator_type, COUNT(*) FROM indicator GROUP BY indicator_type")
    for r in cur.fetchall():
        print(f"    {r[0]}: {r[1]} 条")

    conn.close()


if __name__ == "__main__":
    seed()

